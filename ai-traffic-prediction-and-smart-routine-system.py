ICONS = {"high":"🔴","medium":"🟡","low":"🟢"}

def predict(hour, vehicles):
    # Using a DataFrame with feature names to avoid sklearn warnings
    X = pd.DataFrame([[hour, vehicles]], columns=["hour","vehicles"])
    label_idx = model.predict(X)[0]
    label = le.inverse_transform([label_idx])[0]
    probs = model.predict_proba(X)[0]
    conf  = max(probs)
    return label, conf

def route(start, end, traffic):
    try:
        # Lowercase input for Prolog compatibility
        sol = list(prolog.query(f"suggest({start.lower()},{end.lower()},{traffic.lower()},R,N)"))
        if sol:
            # PySwip returns atoms as bytes or strings; handle list conversion
            path_list = [str(p).upper() for p in sol[0]["R"]]
            path = " → ".join(path_list)
            note = str(sol[0]["N"])
            return path, note
    except Exception as e:
        print(f"Error in Prolog query: {e}")

    return f"{start.upper()} → {end.upper()}", "Direct (Fallback)"

def run(start, end, hour, vehicles):
    traffic, conf = predict(hour, vehicles)
    path, note    = route(start, end, traffic)
    print(f"  {start.upper()} → {end.upper()}  |  {hour:02d}:00  |  {vehicles} vehicles")
    print(f"  Traffic : {ICONS[traffic]} {traffic.upper()}  ({conf:.0%} confidence)")
    print(f"  Route   : {path}")
    print(f"  Note    : {note}")
